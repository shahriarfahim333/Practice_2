fun main (args:Array<String>) {
    var num1:Double  = 7.0
    var num2:Double  = 9.0

    //1. addition
    var add = num1+num2
    println("Addition: ${add}")

    //2. subtraction
    var sub = num1-num2
    println("SUBTRACTION: ${sub}")

    //3. multiplication
    var multi = num1*num2
    println("Multiplication: ${multi}")

    //4. divison
    var div = num1/num2
    println("Divition: ${div}")

    //5. modulus
    var mod = num1%num2
    println("Modulus: ${mod}")


}
